import Foundation
import UIKit

extension UIColor {
    
    class func primaryColor() -> UIColor {
        return UIColor.black//return UIColor(red: 4/255, green: 174/255, blue: 166/255, alpha: 1)
    }
    
    class func secondaryColor() -> UIColor {
        return UIColor(red: 255/255, green: 140/255, blue: 103/255, alpha: 1)
    }
    
}
